func TestRuleService_List_DelegatesToStore(t *testing.T) {
	stub := newStub(
		models.SeasonRule{ID: 1, SeasonID: 1, RuleKey: "allow_substitutes", RuleValue: "true"},
		models.SeasonRule{ID: 2, SeasonID: 1, RuleKey: "games_per_pairing", RuleValue: "3"},
	)
	svc := rules.NewRuleService(stub)
	rows, err := svc.List(context.Background(), 1)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if len(rows) != 2 {
		t.Errorf("want 2 rows, got %d", len(rows))
	}
}
